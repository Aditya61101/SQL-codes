-- DATE FUNCTIONS

-- CURRENT_DATE/CURDATE
SELECT CURRENT_DATE();

-- SYSDATE/NOW
SELECT SYSDATE();
SELECT NOW();

SELECT DATE("2018-01-01 09:34:45"); -- 2018-01-01

SELECT MONTH("2018-10-01 09:34:45"); -- 10

SELECT MONTHNAME("2018-10-01 09:34:45"); -- October

SELECT DAY("2018-10-01 09:34:45"); -- 1

SELECT DAYNAME("2018-10-01 09:34:45"); -- Monday

SELECT YEAR("2018-10-01 09:34:45"); -- 2018

SELECT HOUR("2018-10-01 09:34:45"); -- 9

SELECT MINUTE("2018-10-01 09:34:45"); -- 34

SELECT SECOND("2018-10-01 09:34:45"); -- 45

-- 3 MONTH GRPS
SELECT QUARTER("2018-10-01 09:34:45"); -- 4

SELECT WEEK("2018-10-01 09:34:45"); -- 40

SELECT WEEKDAY("2022-12-19 09:34:45"); -- 0, Monday

SELECT DAYOFYEAR("2018-10-01 09:34:45"); -- 275

SELECT YEARWEEK("2018-10-01 09:34:45");

SELECT LAST_DAY("2018-10-01 09:34:45"); -- 2018-10-31

SELECT EXTRACT(YEAR FROM "2018-10-01 09:34:45"); -- 2018

SELECT EXTRACT(MINUTE_MICROSECOND FROM "2018-10-01 09:34:45"); -- 10

-- ADDDATE/DATE_ADD
SELECT ADDDATE("2018-10-01 09:34:45", INTERVAL 5000 MINUTE); -- 2018-10-02 09:34:45

SELECT SUBDATE("2018-10-01 09:34:45", INTERVAL 5000 MINUTE); -- 2018-10-02 09:34:45

SELECT MAKEDATE(2018, 275); -- 2018-10-01

SELECT DATEDIFF("2022-03-1","2022-12-19"); -- 293

SELECT FROM_DAYS(696969);

-- %D -> nd, rd,st,th
-- %d -> (01-31)
-- %e -> (0-31)
-- %j -> (001-366)
-- %m -> (01-12)
-- %M -> (January-December)
-- %b -> (Jan-Dec)
-- %c -> (1-12)
-- %Y -> (2019)
-- %y -> (19)
-- %a -> (Mon-Sun)
-- %W -> (Monday-Sunday)
-- %w -> (0-6)
SELECT DATE_FORMAT("2019-12-22","%D-%M-%Y");

-- %H -> (00-23)
-- %h -> (01-12)
-- %g -> (1-12)
-- %G -> (0-23)
-- %i(minutes) -> (00-59)
-- %s(seconds) -> (00-59)
-- %p -> (AM/PM)
-- %r -> (09:34:45 AM)
-- %f -> (000000-999999)
SELECT TIME_FORMAT("2019-12-22 09:34:45","%H:%i:%s");

SELECT STR_TO_DATE("10 July 2019","%d %M %Y");

SELECT * FROM emp WHERE hiredate BETWEEN TO_DATE('1981/04/02','yyyy/mm/dd') AND TO_DATE('1981/06/09','yyyy/mm/dd');