-- TIME FUNCTIONS

SELECT CURRENT_TIME();

-- LOCALTIME, LOCALTIMESTAMP
SELECT CURRENT_TIMESTAMP();

SELECT TIME("2019-12-23 12:23:33"); -- 12:23:33

SELECT HOUR("2019-12-23 12:23:33"); -- 12

SELECT MINUTE("2019-12-23 12:23:33"); -- 23

SELECT SECOND("2019-12-23 12:23:33"); -- 33

SELECT MICROSECOND("2019-12-23 12:23:33:3344"); -- 334400

SELECT TIMEDIFF("18:34:55","12:23:33"); -- 06:11:22

SELECT TIMESTAMPDIFF(MINUTE,"2019-12-23 12:23:33","2019-12-23 18:34:55"); -- 371

SELECT ADDTIME("2019-12-23 12:23:33","06:11:22"); -- 2019-12-23 18:34:55

SELECT MAKETIME(16,15,23); -- 16:15:23

SELECT TIMESTAMP("2019-12-23 12:23:33"); -- 2019-12-23 12:23:33

SELECT TIME_FORMAT("2019-12-23 12:23:33","%H:%i:%s"); -- 12:23:33

SELECT TIME_TO_SEC("12:23:33"); -- 44433

SELECT SEC_TO_TIME(44433); -- 12:23:33